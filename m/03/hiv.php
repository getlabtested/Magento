        <h2>What test is offered for AIDS/HIV?</h2>
		<p>The actual test name for determining if HIV is present is called the “Eliza confirmed by Western 
		Blot test.” This blood test screens for the presence of HIV by determining if there are HIV antibodies 
		present in the system.  Most people infected with HIV / AIDS will show a positive test result within 
		3 months of exposure to HIV, using the screening test (repeating Elisa).  If the screening test proves 
		positive, that result is immediately confirmed with the Western Blot confirmation, which uses the same 
		blood sample (you will not be required to give another blood sample).  If you test for HIV before the 
		antibodies have had enough time to appear (this process is called Seroconversion), you may receive a 
		"false negative" result.  If you believe you may have returned a false negative result, we recommend 
		testing again within 6 months.</p> 
        <h2>What kind of sample is required?</h2>
		<p>This HIV Test requires a blood sample which will be drawn by a medical professional at local 
		testing facility of your choosing.</p>
        <h2>How to Prepare for Your Test?</h2>
		<p>No advance preparation is required before your test - just be prepared to have your blood drawn 
		during the visit.</p>
        <h2>When can I expect results?</h2>
		<p>HIV blood test results are usually available in 2-3 business days.  After you purchase your test 
